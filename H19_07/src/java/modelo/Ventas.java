package modelo;

public class Ventas {
    
    private String idVent;
    private String nomCli;
    private String dniCli;
    private String fechVen;
    private String codPer;
    
    private String autPer;

    public String getIdVent() {
        return idVent;
    }

    public void setIdVent(String idVent) {
        this.idVent = idVent;
    }

    
    
    public String getNomCli() {
        return nomCli;
    }

    public void setNomCli(String nomCli) {
        this.nomCli = nomCli;
    }

    public String getDniCli() {
        return dniCli;
    }

    public void setDniCli(String dniCli) {
        this.dniCli = dniCli;
    }

    public String getFechVen() {
        return fechVen;
    }

    public void setFechVen(String fechVen) {
        this.fechVen = fechVen;
    }

    public String getCodPer() {
        return codPer;
    }

    public void setCodPer(String codPer) {
        this.codPer = codPer;
    }

    public String getAutPer() {
        return autPer;
    }

    public void setAutPer(String autPer) {
        this.autPer = autPer;
    }
    
    
    
}
