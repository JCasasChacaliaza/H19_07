package modelo;

public class Producto {
    
    private String idProc;
    private String nomProc;
    private String preProc;
    private String cantProc;
    private String estProc;
    private String fechIngProc;
    private String catProc;

    public String getCatProc() {
        return catProc;
    }

    public void setCatProc(String catProc) {
        this.catProc = catProc;
    }
    
    

    public String getIdProc() {
        return idProc;
    }

    public void setIdProc(String idProc) {
        this.idProc = idProc;
    }

    public String getNomProc() {
        return nomProc;
    }

    public void setNomProc(String nomProc) {
        this.nomProc = nomProc;
    }

    public String getPreProc() {
        return preProc;
    }

    public void setPreProc(String preProc) {
        this.preProc = preProc;
    }

    public String getCantProc() {
        return cantProc;
    }

    public void setCantProc(String cantProc) {
        this.cantProc = cantProc;
    }

    public String getEstProc() {
        return estProc;
    }

    public void setEstProc(String estProc) {
        this.estProc = estProc;
    }

    public String getFechIngProc() {
        return fechIngProc;
    }

    public void setFechIngProc(String fechIngProc) {
        this.fechIngProc = fechIngProc;
    }
    
    
    
}
