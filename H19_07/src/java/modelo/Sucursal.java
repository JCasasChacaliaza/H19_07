package modelo;

public class Sucursal {
    private String idSuc;
    private String telSuc;
    private String dirSuc;
    private String proSuc;//lugar de procedencia
    private String estSuc;

    public String getIdSuc() {
        return idSuc;
    }

    public void setIdSuc(String idSuc) {
        this.idSuc = idSuc;
    }

    public String getTelSuc() {
        return telSuc;
    }

    public void setTelSuc(String telSuc) {
        this.telSuc = telSuc;
    }

    public String getDirSuc() {
        return dirSuc;
    }

    public void setDirSuc(String dirSuc) {
        this.dirSuc = dirSuc;
    }

    public String getProSuc() {
        return proSuc;
    }

    public void setProSuc(String proSuc) {
        this.proSuc = proSuc;
    }

    public String getEstSuc() {
        return estSuc;
    }

    public void setEstSuc(String estSuc) {
        this.estSuc = estSuc;
    }
    
    
    
    
}
