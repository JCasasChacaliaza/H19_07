package modelo;

public class Asignacion {
    private String idAsi;
    private String idPer;
    private String idSuc;
    private String estAsi;
    private String idVen;
    private String gruAsi;//grupo asignado 

    public String getIdAsi() {
        return idAsi;
    }

    public void setIdAsi(String idAsi) {
        this.idAsi = idAsi;
    }
        
    public String getIdPer() {
        return idPer;
    }

    public void setIdPer(String idPer) {
        this.idPer = idPer;
    }

    public String getIdSuc() {
        return idSuc;
    }

    public void setIdSuc(String idSuc) {
        this.idSuc = idSuc;
    }

    public String getEstAsi() {
        return estAsi;
    }

    public void setEstAsi(String estAsi) {
        this.estAsi = estAsi;
    }

    public String getIdVen() {
        return idVen;
    }

    public void setIdVen(String idVen) {
        this.idVen = idVen;
    }

    public String getGruAsi() {
        return gruAsi;
    }

    public void setGruAsi(String gruAsi) {
        this.gruAsi = gruAsi;
    }
    
    
    
    
}
